    @Override
    public void initSplash(ConfigSplash configSplash) {

        final int durationInms = 1 * 1000;
        final int textAnimationDurationInms = 1 * 1000;

        configSplash.setBackgroundColor(R.color.colorPrimary);
        configSplash.setAnimCircularRevealDuration(durationInms);

//        //Customize Logo
//        configSplash.setLogoSplash(R.drawable.vc_splash_icon); //or any other drawable
//        configSplash.setAnimLogoSplashDuration(durationInms); //int ms
//        configSplash.setAnimLogoSplashTechnique(Techniques.FadeInUp);

        configSplash.setPathSplash(PATH); //set path String
        configSplash.setOriginalHeight(520); //in relation to your svg (path) resource
        configSplash.setOriginalWidth(520); //in relation to your svg (path) resource
        configSplash.setAnimPathStrokeDrawingDuration(3000);
        configSplash.setPathSplashStrokeSize(2); //I advise value be <5
        configSplash.setPathSplashStrokeColor(R.color.colorWhite); //any color you want form colors.xml
        configSplash.setAnimPathFillingDuration(3000);
        configSplash.setPathSplashFillColor(R.color.colorSplash); //path object filling color

        //Customize Title
        configSplash.setTitleFont("fonts/roboto_condensed_bold.ttf");
        configSplash.setTitleSplash(getString(R.string.app_name));
        configSplash.setTitleTextColor(R.color.colorWhite);
        configSplash.setTitleTextSize(30f); //float value
        configSplash.setAnimTitleDuration(textAnimationDurationInms);
        configSplash.setAnimTitleTechnique(Techniques.FadeInUp);
    }

